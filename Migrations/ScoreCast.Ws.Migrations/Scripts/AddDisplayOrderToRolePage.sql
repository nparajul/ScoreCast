    CONSTRAINT "FK_match_lineup_match_match_id" FOREIGN KEY (match_id) REFERENCES scorecast.match(id) ON DELETE RESTRICT,
    CONSTRAINT "FK_match_lineup_player_player_id" FOREIGN KEY (player_id) REFERENCES scorecast.player(id) ON DELETE RESTRICT
);
CREATE UNIQUE INDEX IF NOT EXISTS "IX_match_lineup_match_id_player_id" ON scorecast.match_lineup (match_id, player_id);
